#include <stdio.h>

int main() {
    
    /*declaracao de variaveis*/
    int A, B, PROD;
    
    /*leitura dos dados de entrada*/
    scanf("%d %d", &A, &B);
    
    /*calculo do produto*/
    PROD = A * B;
    
    /*impressao do produto*/
    printf("PROD = %d\n", PROD);
    
    return 0;
}